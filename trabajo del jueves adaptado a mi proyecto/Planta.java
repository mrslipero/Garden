public class Planta {
    String identificacion;
    String nombre;
    String especie;
    double precio;
    int stock;

    public Planta(String identificacion, String nombre, String especie, double precio, int stock) {
        this.identificacion = identificacion;
        this.nombre = nombre;
        this.especie = especie;
        this.precio = precio;
        this.stock = stock;
    }
}

